public class literals {
    public static void main(String[] args) {
        int a=123;
        int b=0146;
        int c=0x73b;
        int d=0b1101;
        float e=101.230f;
        float g=123.222f;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(g);
    }
}